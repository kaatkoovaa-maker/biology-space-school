function createLesson(block, title, index) {
  const quizA = block.quizBank[index % block.quizBank.length];
  const quizB = block.quizBank[(index + 1) % block.quizBank.length];
  const resource = lessonResourcesByBlock[block.id]?.[index];
  const theory = resource?.theory || [`${title}. ${block.theory[0]}`, block.theory[1]];
  const details = lessonDetailsById[`${block.id}-${index + 1}`];
  const keyTakeaway =
    details?.examFocus ||
    "ЕГЭ-фокус: по этой теме важно уметь узнавать ключевые признаки, объяснять связь строения и функции и не путать похожие процессы между собой.";

  return {
    id: `${block.id}-${index + 1}`,
    planetId: block.id,
    title,
    videoTitle: block.videoTitle,
    videoPlaceholder: block.videoPlaceholder,
    image: block.image,
    theory: [...theory, details?.deepDive || block.theory[0], keyTakeaway],
    quiz: [
      { ...quizA, id: "q1" },
      { ...quizB, id: "q2" }
    ],
    rewardXp: block.rewardXp + index * 5
  };
}

